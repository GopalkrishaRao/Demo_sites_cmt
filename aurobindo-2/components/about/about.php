                    <p class="about-text">
                        The Rajajinagar Education Society which manages Sri Aurobindo Vidya Mandir was established in the year 1967 by an inspired team of noble young men who dedicated their lives and energy to the cause of Education with the main objective of giving the students a comprehensive & quality education to develop their all round personality with due emphasis on academics, morals, ethical values and discipline.

                        Consequent to the re-organization of the states in 1956, a number of state Government servants coming from integrating area were provided with residential accommodation in Rajajinagar. These Government Servants who thus became residents of Rajajinagar faced many shortcomings, one of which was in sufficient of school facilities and exploitation of parents in the name of education.</p>
                    <p>
                        Based on the principle that "Self help is the best help", some of these government servants joined together and decided to form a society with an object of opening a school to impart good and purposeful education to children and to avoid exploitation of parents.</p>
                    <p>

                        Thus a humble beginning was made by opening the school in June 1969 with Lower Kindergarten and First standard class with 30 children and 3 Teachers. Since then the school has made solid progress and the strength has steadily increased from 30 in 1969 to the present strength of over 3000 in the various units viz., Nursery, Lower Primary, Higher Primary, High School and College.
